    <h1>Tipos de flores e suas funções</h1>
    <p>A flor é a estrutura responsável pela reprodução das plantas angiospermas.

É por meio da reprodução que novas plantas são originadas, assegurando a manutenção dos ecossistemas.</p>

<h2>Funções das flores </h2>
A função primordial das flores é a produção de sementes
para a formação de novas plantas, garantindo a sobrevivência das espécies.

Assim, a flor é responsável pela reprodução das plantas.
Para isso, elas são formadas por folhas modificadas, geralmente de cores atrativas e formatos diferenciados para atrair os seus polinizadores <br>
<h3>Partes das flores</h3>
